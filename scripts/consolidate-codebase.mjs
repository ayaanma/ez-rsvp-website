import fs from "node:fs";
import path from "node:path";

const root = process.cwd();
const touched = [];
const removed = [];
const skipped = [];

function fullPath(...parts) {
  return path.join(root, ...parts);
}

function exists(...parts) {
  return fs.existsSync(fullPath(...parts));
}

function read(relativePath) {
  return fs.readFileSync(fullPath(relativePath), "utf8");
}

function write(relativePath, content) {
  const target = fullPath(relativePath);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, content);
  touched.push(relativePath);
}

function removePath(relativePath) {
  const target = fullPath(relativePath);
  if (!fs.existsSync(target)) return;
  fs.rmSync(target, { recursive: true, force: true });
  removed.push(relativePath);
}

function walk(directory, files = []) {
  const target = fullPath(directory);
  if (!fs.existsSync(target)) return files;
  for (const entry of fs.readdirSync(target, { withFileTypes: true })) {
    if (["node_modules", ".next", ".git", "public"].includes(entry.name)) continue;
    const relativePath = path.join(directory, entry.name);
    if (entry.isDirectory()) walk(relativePath, files);
    else files.push(relativePath);
  }
  return files;
}

function sourceFiles() {
  return [
    ...walk("app"),
    ...walk("components"),
    ...walk("lib"),
    ...walk("types"),
    ...walk("scripts"),
    ...(exists("proxy.ts") ? ["proxy.ts"] : []),
  ].filter((file) => /\.(ts|tsx|js|jsx|mjs|css)$/.test(file));
}

function allSourceText() {
  return sourceFiles()
    .filter((file) => file !== "scripts/consolidate-codebase.mjs")
    .map((file) => {
      try {
        return read(file);
      } catch {
        return "";
      }
    })
    .join("\n");
}

function isReferenced(relativePath) {
  const text = allSourceText();
  const basename = path.basename(relativePath);
  const withoutExtension = relativePath.replace(/\.(ts|tsx|js|jsx|mjs|css)$/, "");
  const aliases = new Set([
    relativePath.replaceAll("\\", "/"),
    withoutExtension.replaceAll("\\", "/"),
    `@/${withoutExtension.replaceAll("\\", "/")}`,
    `./${basename}`,
    `./${basename.replace(/\.(ts|tsx|js|jsx|mjs|css)$/, "")}`,
    basename,
  ]);
  aliases.delete(relativePath);
  return [...aliases].some((alias) => text.includes(alias));
}

function removeIfUnreferenced(relativePath) {
  if (!exists(relativePath)) return;
  if (isReferenced(relativePath)) {
    skipped.push(`${relativePath} is still referenced`);
    return;
  }
  removePath(relativePath);
}

function removeMissingCssImportsFromLayout() {
  const layoutPath = "app/layout.tsx";
  if (!exists(layoutPath)) return;
  const before = read(layoutPath);
  const after = before.replace(/import\s+["']\.\/(.+?\.css)["'];?/g, (match, cssFile) => {
    return exists("app", cssFile) ? match : "";
  });
  if (after !== before) write(layoutPath, after);
}

function consolidateCategories() {
  const mockDataPath = "lib/mock-data.ts";
  if (!exists(mockDataPath)) return;
  const before = read(mockDataPath);
  if (before.includes("export { categories } from \"./categories\"")) return;
  const match = before.match(/export\s+const\s+categories\s*=\s*(\[[\s\S]*?\])\s*;?/);
  if (!match) {
    skipped.push("lib/mock-data.ts categories export was not changed because no simple categories array was found");
    return;
  }
  const categoryLiteral = match[1].trim();
  write("lib/categories.ts", `export const categories = ${categoryLiteral} as const;\n\nexport type EventCategory = (typeof categories)[number];\n`);
  const after = before.replace(match[0], 'export { categories } from "./categories";');
  write(mockDataPath, after);
}

function removeKnownRedundantRoutesAndFiles() {
  removePath("app/app");
  removePath("app/groups");
  removeIfUnreferenced("app/homepage-fixes.css");
  removeIfUnreferenced("app/interaction-fixes.css");
  removeIfUnreferenced("app/ui-fixes.css");
  removeIfUnreferenced("lib/button-actions.ts");
  removeIfUnreferenced("lib/client-actions.ts");
  removeIfUnreferenced("lib/reveal.ts");
}

function removeOldPatchScripts() {
  const scriptsDir = fullPath("scripts");
  if (!fs.existsSync(scriptsDir)) return;
  for (const file of fs.readdirSync(scriptsDir)) {
    const relativePath = path.join("scripts", file);
    if (relativePath === "scripts/consolidate-codebase.mjs") continue;
    if (/patch|fix|remove-groups|cleanup/i.test(file)) removeIfUnreferenced(relativePath);
  }
}

function createReport() {
  const report = [
    "# Codebase cleanup report",
    "",
    "This report was generated by scripts/consolidate-codebase.mjs.",
    "",
    "## Touched files",
    ...(touched.length ? touched.map((item) => `- ${item}`) : ["- None"]),
    "",
    "## Removed files or folders",
    ...(removed.length ? removed.map((item) => `- ${item}`) : ["- None"]),
    "",
    "## Skipped cleanup items",
    ...(skipped.length ? skipped.map((item) => `- ${item}`) : ["- None"]),
    "",
    "Run npm run build and review git diff before committing.",
    "",
  ].join("\n");
  write("CODEBASE_CLEANUP_REPORT.md", report);
}

removeMissingCssImportsFromLayout();
consolidateCategories();
removeKnownRedundantRoutesAndFiles();
removeOldPatchScripts();
createReport();

console.log("Codebase cleanup complete.");
console.log(`Touched: ${touched.length}`);
console.log(`Removed: ${removed.length}`);
if (skipped.length) console.log(`Skipped: ${skipped.length}`);
